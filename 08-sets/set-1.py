# Set is the collection of non-repeatitive elements.
# Set is unordered which means element order doesn't matter.
# Elements in set can't be accessed by the indices.
# Set is immutable.

# Creating a set
s = set()
print(s)
s.add(1)
print(s)


# Creating a set with elements
s = {1, 2, 3, 4, 5}
print(s)