# STRING OPERATIONS


# Concatenation: Strings can be joined by using + operator
a = "Hello "
b = "World"
print(a + b)

# Repeatation: Strings can be repeated using the * operator
z = "Ha"
print(z * 3)

# Membership: Checks for substring using 'in' and 'not in' operator
s = "Python"
print("Do 'Py' lies in Python: ", "Py" in s)