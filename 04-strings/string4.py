# Commonly used string methods, !!! the method doesn't affect the original ones !!!

str = "python is MY favourite Language"

print(str.upper())
print(str.lower())
print(str.capitalize())
print(str.strip())              # remove the leading and trialing whitespaces
print(str.replace('t', 's'))    # replace(old, new)
print(str.split(" "))              # Splits the string into a list by a delimiter (default: space)
