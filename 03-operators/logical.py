# Logical operators are used to combine conditional statements. They return True or False based on the conditions.

condition_a = True
condition_b = False

# and:- returns true if both conditions are true
print(condition_a and condition_b)

# or:- return true if atleast one condition is true
print(condition_a or condition_b)

# not:- reverse the result
print(not condition_a)
print(not condition_b)