# These operators are used to compare two values. They return a boolean value: True or False.

a = 5
b = 3

# Equal to
print(a == b)  # Output: False

# Not equal to
print(a != b)  # Output: True

# Greater than
print(a > b)   # Output: True

# Less than
print(a < b)   # Output: False

# Greater than or equal to
print(a >= b)  # Output: True

# Less than or equal to
print(a <= b)  # Output: False
